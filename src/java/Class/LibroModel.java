package Class;

public class LibroModel {
    private int code;
    private String  nombre;
    private String fecha;
    private String autor;
    private int id;
    
        
    
    public LibroModel(int code,  String nombre, String fecha, String autor, int id){
        this.code=code;
        this.nombre=nombre;
        this.fecha=fecha;
        this.autor=autor;
        this.id=id;
        
        
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    

}
